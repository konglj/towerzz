package com.sdtower.common.bean;

public class UserChargeInfo {

	private int id;

	private String wxid;
	
	private int userlevel;

	private int charge;

	private int getnow;

	private int gettingnow;

	private int allgetting;

	private int score;
	
	private int experience;
	
	
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getWxid() {
		return wxid;
	}

	public void setWxid(String wxid) {
		this.wxid = wxid;
	}

	
	
	
	public int getUserlevel() {
		return userlevel;
	}

	public void setUserlevel(int userlevel) {
		this.userlevel = userlevel;
	}

	public int getCharge() {
		return charge;
	}

	public void setCharge(int charge) {
		this.charge = charge;
	}

	public int getGetnow() {
		return getnow;
	}

	public void setGetnow(int getnow) {
		this.getnow = getnow;
	}

	public int getGettingnow() {
		return gettingnow;
	}

	public void setGettingnow(int gettingnow) {
		this.gettingnow = gettingnow;
	}

	public int getAllgetting() {
		return allgetting;
	}

	public void setAllgetting(int allgetting) {
		this.allgetting = allgetting;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}

	public int getExperience() {
		return experience;
	}

	public void setExperience(int experience) {
		this.experience = experience;
	}

	
	
}
