package com.sdtower.common.bean;

public class TowerType {

	private int id;
	
	private String towertypename;
	
	private String towertypeimg;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTowertypename() {
		return towertypename;
	}

	public void setTowertypename(String towertypename) {
		this.towertypename = towertypename;
	}

	public String getTowertypeimg() {
		return towertypeimg;
	}

	public void setTowertypeimg(String towertypeimg) {
		this.towertypeimg = towertypeimg;
	}

	
}
