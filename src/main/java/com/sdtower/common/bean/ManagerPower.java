package com.sdtower.common.bean;

public class ManagerPower {

	//ID
	private int id;
	
	//角色名称
	private String powerName;
	
	//角色权限
	private String powerQx;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getPowerName() {
		return powerName;
	}

	public void setPowerName(String powerName) {
		this.powerName = powerName;
	}

	public String getPowerQx() {
		return powerQx;
	}

	public void setPowerQx(String powerQx) {
		this.powerQx = powerQx;
	}
	
}
